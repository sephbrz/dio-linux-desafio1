#!/bin/bash

mkdir /publico
mkdir /ven
mkdir /adm
mkdir /sec

echo "Diretórios criados"


groupadd GRP_ADM
groupadd GRP_VEN
groupadd GRP_SEC

echo "Grupos de usuários criados"


chown root:GRP_ADM /adm
chown root:GRP_VEN /ven
chown root:GRP_SEC /sec

chmod 777 /publico
chmod 770 /ven
chmod 770 /adm
chmod 770 /sec

echo "Permissões dos diretórios especificadas"


